package homework.day3.pretask;

public class SimpleMathExample {
    public static void main(String[] args) {
        int length = 5;
        int width = 3;

        if (length > 0 && width > 0) {
            System.out.println(length * width);
        } else {
            System.out.println("Длина и ширина могуты быть только положительными значениями");
        }
    }
}
