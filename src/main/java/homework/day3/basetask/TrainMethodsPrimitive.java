package homework.day3.basetask;

public class TrainMethodsPrimitive {

    public void printInt(int num) {
        System.out.println("я получил на вход число " + num);
    }

    public void printLong(long num) {
        System.out.println("я получил на вход длинное число " + num);
    }

    public void printChar(char symbol) {
        System.out.println("я получил на вход символ " + symbol);
    }

    public void printFloat(float num) {
        System.out.println("я получил на вход дробное число " + num);
    }

    public void printDouble(double num) {
        System.out.println("я получил на вход длинное дробное число " + num);
    }

    public void printShort(short num) {
        System.out.println("я получил на вход короткое число " + num);
    }

    public void printByte(Byte num) {
        System.out.println("я получил на вход очень короткое число " + num);
    }

    public void printBoolean(Boolean bool) {
        System.out.println("я получил на вход булево " + bool);
    }
}
